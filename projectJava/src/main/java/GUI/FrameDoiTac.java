    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int i=jTable1.getSelectedRow();
        
        if(jComboBox1.getSelectedItem().equals("Nhà Cung Cấp"))
        {
            if(i>=0)
            {
               FrameThongTinNCC fNCC = new FrameThongTinNCC();
               DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
               fNCC.txtMaNCC.setText(model.getValueAt(i, 0).toString());
               fNCC.txtMaNCC.setEditable(false);
               fNCC.txtTenNCC.setText(model.getValueAt(i, 1).toString());
               fNCC.txtDiaChi.setText(model.getValueAt(i, 2).toString());
               fNCC.txtSDT.setText(model.getValueAt(i, 3).toString());               
               fNCC.setVisible(true);
               fNCC.setLocationRelativeTo(null);
               fNCC.setResizable(false);

            }
        }
        else 
        {
            FrameThongTinNSX fNSX = new FrameThongTinNSX();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            fNSX.txtMaNSX.setText(model.getValueAt(i, 0).toString());
            fNSX.txtMaNSX.setEditable(false);
            fNSX.txtTenNSX.setText(model.getValueAt(i, 1).toString());
            fNSX.txtQuocGia.setText(model.getValueAt(i, 2).toString());         
            fNSX.setVisible(true);
            fNSX.setLocationRelativeTo(null);
            fNSX.setResizable(false);
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

