    public void hienThi()
    {
        DefaultTableModel model = (DefaultTableModel) jTbNV.getModel();

        
        ArrayList<NhanVien> list = new ArrayList<NhanVien>();
        list = nv.getArrayList();
    
        model.setRowCount(0);
        for(int i = 0 ; i < list.size();i++)
        {
            
//            System.out.println(list.get(i).isTinhTrang());
            model.addRow(new Object[]{list.get(i).getMaNV(),list.get(i).getTen(),list.get(i).getNgSinh(),list.get(i).getDiaChi(),list.get(i).getSdt()} );
        }
    }
