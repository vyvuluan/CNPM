    private void txtTimKiemCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtTimKiemCaretUpdate
        // TODO add your handling code here:
        if(jComboBox1.getSelectedItem().equals("Nhà Cung Cấp"))
        {
            if(jrbMa.isSelected())
            {
                hienthiNCC(bll_ncc.timKiemTheoMa(txtTimKiem.getText()));
            }
            else
            {
                hienthiNCC(bll_ncc.timKiemTheoTen(txtTimKiem.getText()));
            }
        }
        else
        {
            if(jrbMa.isSelected())
            {
                hienthiNSX(bll_nsx.timKiemTheoMa(txtTimKiem.getText()));
            }
            else
            {
                hienthiNSX(bll_nsx.timKiemTheoten(txtTimKiem.getText()));
            }
        }
    }//GEN-LAST:event_txtTimKiemCaretUpdate

