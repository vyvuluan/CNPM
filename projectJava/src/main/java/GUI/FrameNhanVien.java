    private void jTbNVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTbNVMouseClicked
        // TODO add your handling code here:
        int i=jTbNV.getSelectedRow();
        FrameThongTinNhanVien f = new FrameThongTinNhanVien();
        if(i>=0)
        {

            try {
                DefaultTableModel model = (DefaultTableModel) jTbNV.getModel();
                f.txtMaNV.setText(model.getValueAt(i, 0).toString());
                FrameAddAccount.maNV_tmp = model.getValueAt(i, 0).toString();
                f.txtMaNV.setEditable(false);
                f.txtTenNV.setText(model.getValueAt(i, 1).toString());
                Date date1 = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(i, 2).toString());
                f.txtDate.setDate(date1);
                f.txtDiaChi.setText(model.getValueAt(i, 3).toString());
                f.txtSDT.setText(model.getValueAt(i, 4).toString());
                f.setVisible(true);
                f.setLocationRelativeTo(null);
                f.setResizable(false);
            } catch (ParseException ex) {
                Logger.getLogger(FrameNhanVien.class.getName()).log(Level.SEVERE, null, ex);
            }
           
        }
    }//GEN-LAST:event_jTbNVMouseClicked

