    public void hienthiNCC()
    {
         DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        
        ArrayList<NhaCungCap> list = new ArrayList<NhaCungCap>();
        list = bll_ncc.getArrayList();
        model.setColumnCount(0);
        model.addColumn("Mã NCC");
        model.addColumn("tên NCC");
        model.addColumn("Địa chỉ");
        model.addColumn("Số điện thoại");
        model.setRowCount(0);
        for(int i = 0 ; i < list.size();i++)
        {
            

            model.addRow(new Object[]{list.get(i).getMaNCC(),list.get(i).getTenNCC(),list.get(i).getDiaChi(),list.get(i).getSdt()} );
        }
    }
