    public void hienThiTable()
    {
        
        DefaultTableModel model = (DefaultTableModel) jtbTK.getModel();

        
        ArrayList<TaiKhoan> list = new ArrayList<TaiKhoan>();
        list = tk.getArrayList();
        
        model.setRowCount(0);
        for(int i = 0 ; i < list.size();i++)
        {
            
//            System.out.println(list.get(i).isTinhTrang());
            model.addRow(new Object[]{list.get(i).getMaTK(),list.get(i).getMaNV(),list.get(i).getUsername(),list.get(i).getPassword(),list.get(i).getQuyen(),list.get(i).isTinhTrang()} );
        }
        
    }
