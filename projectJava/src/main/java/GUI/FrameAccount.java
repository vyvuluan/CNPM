    private void jtbTKMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbTKMouseClicked
        // TODO add your handling code here:
        int i=jtbTK.getSelectedRow();
        FrameThongTinAccount f = new FrameThongTinAccount();
//        System.out.println(evt.getClickCount());
        if(i>=0)
        {
           DefaultTableModel model = (DefaultTableModel) jtbTK.getModel();
           f.txtmaTK.setText(model.getValueAt(i, 0).toString());
           f.txtmaTK.setEditable(false);
           f.txtmaNV.setText(model.getValueAt(i, 1).toString());
           f.txtmaNV.setEditable(false);
           f.txtTK.setText(model.getValueAt(i, 2).toString());
           f.txtMK.setText(model.getValueAt(i, 3).toString());
           String quyen = model.getValueAt(i, 4).toString();
           f.quyen.setSelectedItem(quyen);
           if(model.getValueAt(i, 5).toString().equals("true")) f.mo.setSelected(true);
           else f.khoa.setSelected(true);
           f.setVisible(true);
           f.setLocationRelativeTo(null);
           f.setResizable(false);
           
        }

    }//GEN-LAST:event_jtbTKMouseClicked

